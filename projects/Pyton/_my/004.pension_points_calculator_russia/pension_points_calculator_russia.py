# ============================================

# –≠—Ç–∏ –¥–∞–Ω–Ω—ã–µ ‚Äî –ø–µ—Ä–∏–æ–¥—ã —Ç–≤–æ–µ–π —Ä–∞–±–æ—Ç—ã –∏ –ø—Ä–∏–º–µ—Ä–Ω–∞—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤ –º–µ—Å—è—Ü –∑–∞ –∫–∞–∂–¥—ã–π –ø–µ—Ä–∏–æ–¥.
# 'start' –∏ 'end' ‚Äî —ç—Ç–æ –≥–æ–¥—ã –Ω–∞—á–∞–ª–∞ –∏ –æ–∫–æ–Ω—á–∞–Ω–∏—è –ø–µ—Ä–∏–æ–¥–∞ (end –Ω–µ –≤–∫–ª—é—á–∞–µ—Ç—Å—è).
# 'salary' ‚Äî —ç—Ç–æ —Å—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤ —ç—Ç–æ—Ç –ø–µ—Ä–∏–æ–¥.
salary_periods = [
    {"start": 2008, "end": 2012, "salary": 18000},
    {"start": 2012, "end": 2013, "salary": 22000},
    {"start": 2013, "end": 2016, "salary": 21000},
    {"start": 2016, "end": 2019, "salary": 30000},
    {"start": 2019, "end": 2020, "salary": 40000},
    {"start": 2021, "end": 2022, "salary": 50000},
    {"start": 2022, "end": 2025, "salary": 67000},
]

# –ü—Ä–∏–º–µ—Ä–Ω—ã–µ —Å—Ä–µ–¥–Ω–∏–µ –∑–∞—Ä–ø–ª–∞—Ç—ã –ø–æ —Å—Ç—Ä–∞–Ω–µ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ –≥–æ–¥—ã
# –≠—Ç–∏ —Ü–∏—Ñ—Ä—ã –≤–ª–∏—è—é—Ç –Ω–∞ —Ç–æ, —Å–∫–æ–ª—å–∫–æ –ø–µ–Ω—Å–∏–æ–Ω–Ω—ã—Ö –±–∞–ª–ª–æ–≤ –º–æ–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å
average_salary_by_year = {
    2008: 17000, 2009: 18000, 2010: 21000, 2011: 23000, 2012: 26000,
    2013: 29000, 2014: 31000, 2015: 32000, 2016: 34000, 2017: 39000,
    2018: 43000, 2019: 47000, 2020: 51000, 2021: 56000, 2022: 62000,
    2023: 66000, 2024: 70000, 2025: 75000
}

# –ú–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ –ø–µ–Ω—Å–∏–æ–Ω–Ω—ã–µ –±–∞–ª–ª—ã, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å –≤ –≥–æ–¥ –ø–æ –∑–∞–∫–æ–Ω—É
max_points_per_year = {
    2008: 6.28, 2009: 6.81, 2010: 6.11, 2011: 6.19, 2012: 6.24,
    2013: 6.16, 2014: 6.60, 2015: 7.39, 2016: 7.83, 2017: 8.26,
    2018: 8.70, 2019: 9.13, 2020: 9.57, 2021: 10.00, 2022: 10.00,
    2023: 10.00, 2024: 10.00, 2025: 10.00
}

# –ó–¥–µ—Å—å –º—ã –±—É–¥–µ–º —Ö—Ä–∞–Ω–∏—Ç—å –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±–∞–ª–ª–æ–≤
total_points = 0

# –ê —Ç—É—Ç ‚Äî —Å–∫–æ–ª—å–∫–æ –±–∞–ª–ª–æ–≤ –∑–∞ –∫–∞–∂–¥—ã–π –≥–æ–¥
yearly_points = {}

# –û—Å–Ω–æ–≤–Ω–æ–π —Ä–∞—Å—á—ë—Ç
for period in salary_periods:
    for year in range(period["start"], period["end"]):
        avg_salary = average_salary_by_year.get(year, 50000)  # —Å—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤ —Å—Ç—Ä–∞–Ω–µ –≤ —ç—Ç–æ–º –≥–æ–¥—É
        salary = period["salary"]                             # —Ç–≤–æ—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤ —ç—Ç–æ–º –≥–æ–¥—É
        ratio = salary / avg_salary                           # –≤–æ —Å–∫–æ–ª—å–∫–æ —Ä–∞–∑ —Ç–≤–æ—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –≤—ã—à–µ/–Ω–∏–∂–µ —Å—Ä–µ–¥–Ω–µ–π
        base_points = 6.0                                     # –±–∞–∑–æ–≤–∞—è –æ—Ü–µ–Ω–∫–∞: –µ—Å–ª–∏ –∑–ø = —Å—Ä–µ–¥–Ω–µ–π, —Ç–æ 6 –±–∞–ª–ª–æ–≤
        points = ratio * base_points                          # –≤—ã—á–∏—Å–ª—è–µ–º –±–∞–ª–ª—ã –∑–∞ –≥–æ–¥
        max_points = max_points_per_year.get(year, 10.0)      # –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–µ –±–∞–ª–ª—ã –≤ —ç—Ç–æ–º –≥–æ–¥—É
        final_points = min(points, max_points)                # –Ω–µ–ª—å–∑—è –ø–æ–ª—É—á–∏—Ç—å –±–æ–ª—å—à–µ –º–∞–∫—Å–∏–º—É–º–∞
        yearly_points[year] = round(final_points, 2)          # —Å–æ—Ö—Ä–∞–Ω—è–µ–º –≤ —Å–ª–æ–≤–∞—Ä—å
        total_points += final_points                          # –¥–æ–±–∞–≤–ª—è–µ–º –∫ –æ–±—â–µ–π —Å—É–º–º–µ

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
print(f"üìä –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–µ–Ω—Å–∏–æ–Ω–Ω—ã—Ö –±–∞–ª–ª–æ–≤: {round(total_points, 2)}")
print("üìÖ –ë–∞–ª–ª—ã –ø–æ –≥–æ–¥–∞–º:")
for year, points in sorted(yearly_points.items()):
    print(f"  {year}: {points} –±–∞–ª–ª–æ–≤")
