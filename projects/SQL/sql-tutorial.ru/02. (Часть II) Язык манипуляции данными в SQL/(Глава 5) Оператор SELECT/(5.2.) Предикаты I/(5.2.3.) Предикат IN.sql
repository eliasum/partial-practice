Пример 5.2.5

Найти модель, частоту процессора и объем жесткого диска компьютеров, которые комплектуются накопителями 10 Гбайт или 20 Гбайт и выпускаются
 производителем А:
*/

    SELECT model, speed, hd
    FROM PC 
    WHERE hd IN (10, 20) AND 
     model IN (SELECT model 
     FROM product 
     WHERE maker = 'A'
     );

/*
